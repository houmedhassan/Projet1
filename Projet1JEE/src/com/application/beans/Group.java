public class Group {
	private long idGroup;
	private String name;
	/**
	 * Constructor
	 */
	public Group(){
		
	}
	/**
	 * @return idGroup
	 */
	public long getIdGroup() {
		return idGroup;
	}
	/**
	 * @param idGroup
	 */
	public void setIdGroup(int idGroup) {
		this.idGroup = idGroup;
	}
	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name
	 */
	public void setName(String name) {
		this.name = name;
	}
}
